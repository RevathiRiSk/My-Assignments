package javaday;

public class Swapping{

	public static void main(String[] args) {
		
	
		//Write a Java program to swap two variables.
		
		int a = 12;
		int b = 24;
		System.out.println("Before swapping a,b : " +a +","+ b);
		
		a = a+b;
		b = a-b;
		a = a-b;
		
		System.out.println("After swapping a,b :" +a +","+b);
		 
	
		
	}
	
		
	}		
		
	


